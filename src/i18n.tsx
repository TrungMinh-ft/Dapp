function getInitialLanguage(): Language {
  if (typeof window === "undefined") return "en";
  const stored = window.localStorage.getItem(languageStorageKey);
  return stored === "vi" || stored === "en" ? stored : "en";
}

export function getCurrentLanguage(): Language {
  return getInitialLanguage();
}

export function translate(key: string, params?: Record<string, string | number | null | undefined>, language = getCurrentLanguage()) {
  const value = dictionaries[language][key] ?? dictionaries.en[key] ?? key;
  return typeof value === "function" ? value(params ?? {}) : value;
}

type I18nContextValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string, params?: Record<string, string | number | null | undefined>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(getInitialLanguage);

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage: (nextLanguage) => {
        window.localStorage.setItem(languageStorageKey, nextLanguage);
        setLanguageState(nextLanguage);
      },
      t: (key, params) => translate(key, params, language),
    }),
    [language],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }

  return context;
}

export function formatStatusLabel(value: string, t: I18nContextValue["t"]) {
  const labels: Record<string, string> = {
    "VOTING LIVE": t("status.votingLive"),
    PASSED: t("status.passed"),
    REJECTED: t("status.rejected"),
    FINISHED: t("status.finished"),
    "PUBLIC VOTE": t("status.publicVote"),
    "OASIS ENCRYPTED": t("status.oasisEncrypted"),
  };
  return labels[value] ?? value;
}

export function formatCandidateName(value: string | null | undefined, t: I18nContextValue["t"]) {
  if (!value || value === "N/A") return t("common.none");
  if (value === "NO") return t("candidate.no");
  if (value === "YES") return t("candidate.yes");
  if (value === "Đồng ý") return t("candidate.yes");
  if (value === "Không đồng ý") return t("candidate.no");
  return value;
}
